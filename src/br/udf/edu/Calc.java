    private void EqualButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualButtonActionPerformed
        // TODO add your handling code here:
        
        try{
            number2 = Double.parseDouble(TextFieldCalc.getText());
            if (operator == "+") {
                operation.secondNumber = number2;
                TextFieldCalc.setText(String.valueOf(operation.sumNumbers()));
            } else if (operator == "-") {
                operation.secondNumber = number2;
                TextFieldCalc.setText(String.valueOf(operation.minusNumbers()));
            } else if (operator == "*") {
                operation.secondNumber = number2;
                TextFieldCalc.setText(String.valueOf(operation.timesNumbers()));
            } else if (operator  == "/") {
                operation.secondNumber = number2;
                TextFieldCalc.setText(String.valueOf(operation.divisionNumbers()));
            }
        }catch(Exception error){
            TextFieldCalc.setText("Error!");
            Timer time = new Timer(1000,e->{
                TextFieldCalc.setText("");
                Timer equalTimerErr = (Timer)e.getSource();
                equalTimerErr.stop();
            });
            time.start();
        }

    }//GEN-LAST:event_EqualButtonActionPerformed

